    '  continue reads --routing-policy as input and writes model-routing-policy.next.json',
    '  plan/run consume model-routing-policy.next.json with --routing-policy <file>',
    '  --dependency-health=false --dependency-health-out <file>',
    '  distributed-pilot --out-dir <dir> --run-id <id> --repo <dir> --repo <dir>',
	    '  run --distributed writes coordinator and worker .frontier-run/<runId> ledgers plus a distributed-run proof',
	    '  continue --distributed syncs distributed peers and records answer/next-wave decisions in that ledger',
    '  --distributed-run-root <dir> --distributed-run-peer <run-dir> --distributed-run-transport local-fs|git-repo',
    '  --distributed-run-sync-direction pull|push|bidirectional --distributed-run-proof <file|false>',
    '  --run-events <file|false> --run-dashboard <file|false> (current frontier-run format)',
    '  sync --run <dir|swarm-results.json|run-events.jsonl> --peer <dir|swarm-results.json|run-events.jsonl>',
    '  --sync-peer <path> --run-sync-direction pull|push|bidirectional',
